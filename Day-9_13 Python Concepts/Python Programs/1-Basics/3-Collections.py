# Python Collections

# 1. List: It is a collection of ordered and changeable data. Allow Duplicate Data
listData = [1,2,3,4,5]

# 2. Tuple:It is a collection of ordered and unchangeable data. Allow Duplicate Data
tupleData = (1,2,3,4,5)

# 3. Set:It is a collection of un-ordered and un-changeable data. Not Duplicate Data
setData =  {1,2,3,4,5} 

# 4. Dictionary:It is a collection of ordered and changeable data. No Duplicate Member
dictData = {"key1": 15, "key2":20, "key3":30, "key3":40}
print(dictData)
